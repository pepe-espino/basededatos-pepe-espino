JOIN ALUMNO a ON P.DNI = A.DNI 
JOIN ALUMNO_ASIGNATURA aa ON A.IDALUMNO = AA.IDALUMNO 
WHERE AA.IDASIGNATURA IN ('150212', '130113');

-- 16

